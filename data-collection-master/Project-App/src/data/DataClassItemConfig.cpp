void init_appdata(void)
{
    for (int i = 0; i < A.size(); i++)
    {
        switch (A(i).config.format)
        {
        case DF_BCD:
            A(i).val = 0;
            break;
        case DF_HEX:
            memset(A(i).hex, 0, A(i).len());
            break;
        }
    }
}

//格式:  "rain:waterlevel:flow:",以":"分隔
bool jia_add_flags(string name)
{
    vector<string>::iterator it;
    vector<string> vec = A["jia"].list;
    it = find(vec.begin(), vec.end(), name);
    if (it != vec.end())
        return true;
    else
    {
        A["jia"].list.push_back(name);
        INFO("[加报标志]: %s", name.c_str());
    }

    return true;
}