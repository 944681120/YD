{
    vector<string>::iterator it;
    vector<string> vec = A["jia"].list;
    it = find(vec.begin(), vec.end(), name);
    if (it != vec.end())
        return true;
    else
    {
        A["jia"].list.push_back(name);
        INFO("[加报标志]: %s", name.c_str());
    }

    return true;
}